def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  dif = num1 - num2
  return dif
end

def division(num1, num2)
  quotient = num1 / num2
  return quotient
end

def multiplication(num1, num2)
  product = num1 * num2
  return product
end

def modulo(num1, num2)
  remainder = num1 % num2
  return remainder
end

def square_root(num)
  root = num ** (1/2)
  return root
end